    <!-- Simple AOS and Lucide Initialization -->
    <script>
        // Simple initialization
        function initWildernest() {
            // Initialize AOS with fallback
            if (typeof AOS !== 'undefined') {
                try {
                    AOS.init({
                        duration: 800,
                        easing: 'ease-in-out',
                        once: true,
                        offset: 50,
                        delay: 0,
                        disable: false,
                        startEvent: 'DOMContentLoaded'
                    });

                    // Multiple refresh attempts
                    setTimeout(() => {
                        AOS.refresh();
                    }, 100);

                    setTimeout(() => {
                        AOS.refresh();
                    }, 500);

                } catch (error) {
                    // If AOS fails, ensure content is visible
                    document.querySelectorAll('[data-aos]').forEach(el => {
                        el.style.opacity = '1';
                        el.style.transform = 'none';
                        el.style.visibility = 'visible';
                    });
                }
            } else {
                // AOS not loaded, make sure content is visible
                setTimeout(() => {
                    document.querySelectorAll('[data-aos]').forEach(el => {
                        el.style.opacity = '1';
                        el.style.transform = 'none';
                        el.style.visibility = 'visible';
                    });
                }, 100);
            }

            // Initialize Lucide
            if (typeof lucide !== 'undefined') {
                try {
                    lucide.createIcons();
                } catch (error) {
                    // Silent error handling
                }
            }

            // Scroll to top button
            const scrollBtn = document.getElementById('scroll-to-top');
            if (scrollBtn) {
                window.addEventListener('scroll', () => {
                    scrollBtn.style.opacity = window.pageYOffset > 300 ? '1' : '0';
                    scrollBtn.style.pointerEvents = window.pageYOffset > 300 ? 'auto' : 'none';
                });

                scrollBtn.addEventListener('click', () => {
                    window.scrollTo({ top: 0, behavior: 'smooth' });
                });
            }
        }



        // Immediate visibility fix for hero content
        document.querySelectorAll('.hero-title, .hero-subtitle, .hero-buttons').forEach(el => {
            el.style.opacity = '1';
            el.style.visibility = 'visible';
            el.style.transform = 'none';
        });

        // Initialize when DOM is ready
        if (document.readyState === 'loading') {
            document.addEventListener('DOMContentLoaded', initWildernest);
        } else {
            initWildernest();
        }

        // Backup initialization
        window.addEventListener('load', () => {
            setTimeout(initWildernest, 500);
        });

        // Emergency fallback - ensure hero is visible after 1 second
        setTimeout(() => {
            document.querySelectorAll('.hero-title, .hero-subtitle, .hero-buttons, [data-aos]').forEach(el => {
                el.style.opacity = '1';
                el.style.visibility = 'visible';
                el.style.transform = 'none';
            });
        }, 1000);
    </script>
